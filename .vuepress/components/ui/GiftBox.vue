<template>
    <b-card no-body :img-src="$withBase(image)" class="shadow border-0 rounded-0">
        <b-card-body>
            <h5>Dear {{ gift.content.receiver }},  <b-badge v-if="gift.id" :href="tokenExplorerLink" target="_blank" variant="secondary" class="float-right">#{{ gift.id }}</b-badge></h5>
            <p class="card-text pt-4 pb-3">{{ gift.content.message }}</p>
            <h6>{{ gift.content.sender }}</h6>
            <small>{{ formattedDate }}</small>
        </b-card-body>
        <b-card-footer v-if="gift.amount > 0">
            <small class="text-muted">
                You received <strong>{{ gift.amount }} ETH</strong>
            </small>
        </b-card-footer>
    </b-card>
</template>

<script>
  export default {
    name: 'GiftBox',
    props: {
      gift: {
        type: Object,
        default: {},
      },
      network: {
        type: Object,
        default: {},
      },
    },
    computed: {
      image () {
        return 'assets/images/' + (this.gift.style ? `styles/${this.gift.style}.jpg` : 'cryptogift-header.jpg');
      },
      formattedDate () {
        return new Date(this.gift.date).toLocaleString();
      },
      tokenExplorerLink () {
        return this.network.etherscanLink + '/token/' + __TOKEN_ADDRESS__ + '?a=' + this.gift.id;
      },
    },
  };
</script>
